										<h2> <span>Research Symposium April-May 2018</span></h2>
									</div>
                                    <p><a style="display: inline-flex;" href="https://www.landolakes.org" target="_blanc">Land O’Lakes International Development</a> is implementing the <a style="display: inline-flex;" href="https://www.landolakes.org/where-we-work/africa/rwanda/cooperative-development-program-(1)" target="_blanc">Cooperative Development Program (CDP)</a> in Rwanda funded through the <a style="display: inline-flex;" href="https://www.usaid.gov" target="_blanc">United States Agency for International Development (USAID)</a>. The Cooperative Development Program works to increase access to self-reliant cooperative enterprises that meet the evolving needs of their members and contribute to improved quality of life for members, especially women, through the formation and increased competitiveness of self-sustaining horizontally and/or vertically integrated dairy cooperatives.</p>

<p>CDP is also working with a Rwandan university, the <a style="display: inline-flex;" href="http://www.unilak.ac.rw" target="_blanc">University of Lay Adventists of Kigali (UNILAK)</a> on a local research initiative to study governance, youth, gender and disabilities inclusion within rice cooperatives in Rwanda, aimed at strengthening UNILAK’s outreach programs and Rwandan-owned cooperative research in the process.</p>

<p>On May 22-24, 2018, UNILAK will host an International Research Symposium along with support from the USAID funded Land O’Lakes CDP. The symposium will bring together researchers from Rwanda, East Africa and beyond, for 3 full-days consisting of a full-day conference, expert-led training on relevant research skills, and an interactive learning tour to UNILAK’s campus in the Southern Province.</p>

<p>The first day of the event will be a full-day conference at a venue to be selected in Kigali, under the theme: RURAL RESILIENCE - THE ROLE OF COOPERATIVES IN ACHIEVING SUSTAINABLE RURAL DEVELOPMENT. UNILAK issued a call for abstracts, selected finalists to submit full papers, and reviewed and selected the best papers to be presented at the conference.</p>

<p>Days 2 and 3 are by special invitation, due to attendance constraints.</p>

<p>Day 2 of the event is planned to include a visit to UNILAK’s Nyanza campus in the Southern Province and a presentation by a professor from Moshi University in Tanzania on creating a cooperative center excellence, based on his own experience at Moshi.</p>

<p>Day 3 of the event will be hosted at UNILAK’s Kigali campus, and will feature lecture and hands-on learning sessions pertaining to qualitative research methods, utilizing research findings to create an outreach curriculum, and ethics in research and publishing.</p>

