									</div>
									<article>
										 
										<p>This panel will discuss lessons learned in managing various recent public health emergencies. It underscores the fact that public health emergencies have the potential to threaten African (indeed global) peace and security, disrupt national economic activities, and destroy health systems and communities.</p>
<p>The panel will focus on:</p>
<ul class="spip"><li> Moving towards stronger emergency preparedness with emphasis on prevention through risk management and through active engagement with the community;</li><li> Addressing the social and environmental determinants that influence vulnerability, risk and outcomes related to health emergencies; and</li><li> Developing capacities for early detection, rapid response and prompt recovery including global advocacy, health systems strengthening and social mobilization of people, communities and the private sector.</li></ul>
