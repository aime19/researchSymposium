<p>The organising committee welcomes you to the Cooperative Research Symposium under the theme; Rural Resilience – The role of cooperatives in achieving sustainable rural development. This symposium is taking place on the 22nd May, 2018 at the Marriott Hotel in Kigali with registration at 8:00 a.m. and opening ceremonies starting at 8.30 a.m.</p>
<p>The logistics information provided will help you better prepare for your participation but should you need any further assistance, please do not hesitate to contact Younouss Uwase on younouss.u@theeventsfactory.biz. </p>

<h2><span style="text-transform: none;">ARRIVAL</span></h2>
<ol>
<li>Single Entry Visas:</li>
<p>Since January 2018, Citizens of all countries get their visas on arrival upon payment of a 30 dollar visa fee (Payable online on; https://irembo.gov.rw). The exception is for those countries where there is a reciprocity agreement waiving this fee.  For more information please visit https://www.migration.gov.rw 
Getting to Rwanda
Kigali International Airport connects the world to Rwanda with flights to and from:

Addis Ababa, Accra, Amsterdam, Arusha, Brazzaville, Brussels, Bujumbura, Dar es Salaam, Doha, Dubai, Entebbe, Istanbul, Johannesburg, Juba, Kilimanjaro, Lagos, Libreville, Yaoundé, Cotonou, Douala, Tambo, Mumbai, Mombasa and Nairobi.
</p>
<li>Airport Transfers:</li>
<p>
Upon your arrival at Kigali International Airport, you will be received by a team of ushers who will assist you to get to your hotel. This service will only be available on May 21st 2018, delegates arrive at other dates will be encouraged to make own arrangements.
Program</p>
<p>Day 1: May 22nd, 2018:	Full day Symposium for all delegates and participants invited</p>
<p>Day 2: May 23rd, 2018:	Full day workshop at UNILAK Nyanza Campus ONLY for selected delegates invited to participate in the workshop.</p>
<p>Day 3: May 24th, 2018:	Full day Training for selected participants invited for the training</p>
<li>Weather:</li>
<p>The symposium is happening at the end of a rain season. It would be a good idea to have the appropriate clothing for this kind of weather however, temperatures average around 24°C (75°F), except for in the higher mountain areas where it ranges from 10°C to 15°C (50°F to 60°F)

Rwanda is a polythene-free country. For environmental reasons, plastic bags have been banned, the customary plastic duty-free bags included.

For more information go to: www.rwandatourism.com</p>
<li>Getting around Kigali: </li>
<p>Short distances within the city can be travelled either on foot or by taxi-velo (bicycle taxi). These are relatively inexpensive.
One can also hire a special taxi by calling them or if at any hotel, reception can call a recommended driver for you. These are more expensive than motorcycle taxis and other public buses.
Plugs and Voltage
Electricity in Rwanda is 230 Volts. If you have a device that does not accept 230 Volts at 50 Hertz, you will need a voltage converter. Plugs have two round pins. Please be sure to carry with you a multiple travel adaptor. 
</p>
<li>Telephone and SIM cards:</li>
<p>Rwanda has various phone service providers. Should you require a SIM card for usage whilst here in Kigali, you will need the following:</p>

<p>- Valid Passport or proof of identification </p>
<p>- Address in Kigali - Kigali, Gacuriro/Gasabo District</p>
<p>- 1500 Rwf+ funds for desired level of airtime</p>

<h2><span style="text-transform: none;">MONEY</span></h2>

<li>Currency:</li>
<p>Rwanda’s unit of currency is the Rwandan franc (Rwf). It is advised to arrive in Rwanda with US dollars or Euros in cash, which can be exchanged either at the airport or at any FOREX bureau or bank. The rates indicated below are indicative and may vary slightly</p>
<p>1 US Dollar = 865 Rwandan Francs</p>
<p>1 Euro = 870 Rwandan Francs</p>
<p>1 Pound = 1,010.12 Rwandan Francs</p>
<p>(NB:  Exchange rates as of March 4, 2017 Rwanda National Bank)</p>

<p>There are ATMs at the airport and throughout Kigali that are accessible. Credit cards are accepted in some upmarket restaurants and hotels, but it is best to confirm prior to ordering. There is Exchange Forex Bureau at the Airport or you can visit any banking institution.</p>
<p>All banks and most bureaux de changes are closed on Sundays, and on the last Saturday of every month until 12:30 pm for the national day of community work, “UMUGANDA”.</p>
<p>HEALTH AND INTERNATIONAL TRAVEL</p>

<li>Health:</li>

<p>Travellers are kindly requested to have with them their international vaccination card for all travel to Africa and within Africa. In most African countries, you are required to have a valid Yellow Fever Vaccine. You may be denied entry if you fail to present your card or if your immunization is not valid.</p>
<p>Yellow fever is a risk in Rwanda. It is recommended that all travellers over the age of 9 months receive this vaccine. This is also important for travellers who will continue on to other EAC countries since Uganda, Tanzania and Kenya all require yellow fever vaccinations as well.</p>

<p>The following vaccinations are recommended but not required when visiting Rwanda:</p>
<p>- Hepatitis A</p>
<p>- Hepatitis B</p>
<p>- Malaria</p>
<p>- Typhoid</p>
<p>- Rabies</p>

<p>Even though less prevalent than other African countries, Malaria does exist in Rwanda. While there is no vaccine against malaria, prophylactic drugs along with prevention will significantly reduce the risk of contracting it.</p>

<p>We recommend you consult your doctor or clinic on malaria medicine prior to traveling to Rwanda.</p>

