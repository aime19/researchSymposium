      	<h3 class="modal-title" style="color: #0860a0;">Professor Sir Michael G. Marmot MBBS, MPH, PhD, FRCP, FFPHM, FMedSci, FBA</h3>
      	<h4 style="    color: #676767;    font-size: 14px;">Director of the Institute of Health Equity (UCL Department of Epidemiology & Public Health)</h4>
        <p><p>Sir Michael Marmot is Professor of Epidemiology at University College London, and Immediate Past President of the World Medical Association.</p>
<p>He is the author of The Health Gap: the challenge of an unequal world (Bloomsbury: 2015) and Status Syndrome: how your place on the social gradient directly affects your health (Bloomsbury: 2004).  Professor Marmot holds the Harvard Lown Professorship for 2014-2017 and is the recipient of the Prince Mahidol Award for Public Health 2015. He has been awarded honorary doctorates from 18 universities. Marmot has led research groups on health inequalities for 40 years.</p>
<p>He chairs the Commission on Equity and Health Inequalities in the Americas, set up in 2015 by the World Health Organizations’ Pan-American Health Organization (PAHO/ WHO).  He was Chair of the Commission on Social Determinants of Health (CSDH), which was set up by the World Health Organization in 2005, and produced the report entitled: ‘Closing the Gap in a Generation’ in August 2008.</p>
<p>At the request of the British Government, he conducted the Strategic Review of Health Inequalities in England post 2010, which published its report &#8217;Fair Society, Healthy Lives&#8217; in February 2010. This was followed by the European Review of Social Determinants of Health and the Health Divide, for WHO Euro in 2014.  He chaired the Breast Screening Review for the NHS National Cancer Action Team and was a member of The Lancet-University of Oslo Commission on Global Governance for Health.</p>
<p>He set up and led a number of longitudinal cohort studies on the social gradient in health in the UCL Department of Epidemiology &amp; Public Health (where he was head of department for 25 years): the Whitehall II Studies of British Civil Servants, investigating explanations for the striking inverse social gradient in morbidity and mortality; the English Longitudinal Study of Ageing (ELSA), and several international research efforts on the social determinants of health.</p>
<p>He served as President of the British Medical Association (BMA) in 2010-2011, and is President of the British Lung Foundation.  He is an Honorary Fellow of the American College of Epidemiology; a Fellow of the Academy of Medical Sciences; an Honorary Fellow of the British Academy, and an Honorary Fellow of the Faculty of Public Health of the Royal College of Physicians.  He was a member of the Royal Commission on Environmental Pollution for six years and in 2000 he was knighted by Her Majesty The Queen, for services to epidemiology and the understanding of health inequalities.  Professor Marmot is a Member of the National Academy of Medicine.</p></p>
