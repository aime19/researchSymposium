									<article>
										 
										<p><strong>Background</strong></p>
<p>While facing a huge burden of communicable diseases, new threats such as non-communicable diseases and the impact of climate change are jeopardising the human, social and economic development of African populations. Equitable access to health care and prevention remains a distant goal in most countries. The demographic transition in the continent represents both an opportunity as well as a threat especially if suitable development options for healthcare and prevention are not adopted and implemented. The panel discussion will aim to inspire Africa led action to address challenges related to communicable, non-communicable diseases and environmental determinants of health.</p>
