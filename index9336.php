        <p><p>Dr. Felicitas Zawaira is the new Director of the Family and Reproductive Health Cluster at the WHO Regional Office for Africa, a position she was appointed to in July 2015.  She leads a team of technical staff working at the Regional Office and in Inter-country support teams in the areas of Reproductive and Women’s Health; Child, Adolescent Health and Nutrition; as well as Immunization and Vaccinepreventable Diseases.</p>
<p>Dr. Zawaira’s professional background is in medicine and public health.  She worked as director of maternal and child health for seven years and as principal medical director for another two years, coordinating the planning, resourcing and running of the entire preventive and curative services in the Ministry of Health in Zimbabwe.</p>
<p>Joining WHO in 1998, Dr. Zawaira worked in the country office in Zimbabwe and thereafter in the Regional Office for Africa in the areas of HIV and Women’s Health.  She has served as Head in the following WHO country offices: Zanzibar, Uganda, Sierra Leone, Malawi and Botswana where she mentored and led WHO teams to produce results.</p>
<p>Felicitas holds a Bachelor of Science degree in Biochemistry and an MD in Medicine and Surgery from the University of Zimbabwe; a Masters in Maternal and Child Health from the University of London and a diploma in Management Methods for International Health from the University of Boston, Massachusetts.  She is a Member of the Institute of Child Health, London.</p></p>
