									<article>
										 
										<p><strong>Background</strong></p>
<p>Strengthening existing partnerships and creating new connections will be key to attaining the goal of Universal Health Coverage – i.e. that all people obtain the health services they need without suffering financial hardship when paying for them. To achieve this goal, partnering with the private sector is fundamental. Finding new approaches to utilising partnerships has to come from all levels of government and beyond. Policy makers have to open the door for collaboration with the private sector, and partners have to create innovative solutions that can expand access to health services in new ways. The panel will discuss the role, importance and added value of private sector engagement in the context of Africa’s efforts to achieve universal health coverage.</p>
