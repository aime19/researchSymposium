        <p><p>Dr Matshidiso Moeti from Botswana is the first woman WHO Regional Director for Africa. She aims to build a responsive, effective and result-driven regional secretariat that can advance efforts towards universal health coverage and accelerate progress toward global development goals, while tackling emerging threats. Strong partnerships will underpin every aspect of the Regional Office&#8217;s work during her tenure.</p>
<p>Dr Moeti is a public health veteran, with more than 35 years of national and international experience. She joined WHO’s Africa Regional Office in 1999 and has served as Deputy Regional Director, Assistant Regional Director, Director of Noncommunicable Diseases, WHO Representative for Malawi, and Coordinator of the Inter-Country Support Team for the South and East African countries.</p>
<p>Prior to joining WHO, she worked with UNAIDS as a Team Leader of the Africa and Middle East Desk in Geneva (1997-1999); with UNICEF as a Regional Health Advisor for East and Southern Africa; and with Botswana’s Ministry of Health as a Clinician and Public Health Specialist.</p>
<p>Notably, at the height of the HIV/AIDS epidemic, she led the WHO Regional Office for Africa&#8217;s efforts on treatment scale-up in the context of the ‘3 by 5’ initiative and established a regional HIV laboratory network resulting in a significant increase in the number of HIV-positive individuals accessing antiretroviral therapy in the Region. She also successfully spearheaded the development of WHO Regional Strategies for public health priority areas, including communicable and non-communicable diseases, immunization, maternal and child health, and the health systems strengthening.</p>
<p>Dr Moeti qualified in medicine (M.B., B.S) and public health (MSc in Community Health for Developing Countries) at the Royal Free Hospital School of Medicine, University of London in 1978 and the London School of Hygiene and Tropical Medicine in 1986, respectively.</p></p>
