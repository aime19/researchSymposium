									<article>
										 
										<p><strong>Background</strong></p>
<p>Countries in the African region have made commendable strides in health,however the newly adopted Sustainable Development Goals require more efforts to ensure healthy lives and wellbeing for all. It is critical to improve the scope and mode of delivery of health services and also to address the existing inequities in order to have a meaningful impact on people’s lives. The health sector interventions will need to be complemented by inter-sectorial action to effectively address the social, economic and environmental determinants of health. At the end it is important to identify ways countries can optimise the multiplicity of stakeholders and the different resources to build a resilient health system that will sustainably address the health and wellbeing of the people without leaving anyone behind.</p>
